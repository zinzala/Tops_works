// 2. Write a program to make addition, Subtraction, Multiplication and
// Division of Two Numbers.
void main(){
  int first_number = 20;
  int second_number = 15;

  print('Addition of $first_number and $second_number is : ${first_number + second_number}');
  print('subtraction of $first_number and $second_number is : ${first_number - second_number}');
  print('multiplication of $first_number and $second_number is : ${first_number * second_number}');
  print('Division of $first_number and $second_number is : ${first_number / second_number}');

}
